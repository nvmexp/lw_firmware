/*!
 * @brief  Retrieve the SL3000 device cert template
 *
 * @param[in]  ppCert           The pointer points the buffer storing the address
 *                              of the cert template
 * @param[in]  pCertSize        The pointer points the buffer storing the size of
 *                              the cert template
 * @param[in]  pDevCertIndices  The pointer points the buffer storing the field
 *                              indices into the cert template
 */
void
prGetSL3000DeviceCertTemplate_GV10X
(
    LwU8  **ppCert,
    LwU32  *pCertSize,
    void   *pDevCertIndices
)
{
    DRM_TEE_LPROV_CERT_INDEXES *pDTLCI = (DRM_TEE_LPROV_CERT_INDEXES *)pDevCertIndices;

    *ppCert    = s_rgbDeviceCertTemplate_SL3000_GV10X;
    *pCertSize = sizeof(s_rgbDeviceCertTemplate_SL3000_GV10X);

    pDTLCI->ibCertID                                             = OEM_TEE_LPROV_DEVICECERT_CERTID_OFFSET_SL3000_GV10X;
    pDTLCI->ibCertClientID                                       = OEM_TEE_LPROV_DEVICECERT_CLIENTID_OFFSET_SL3000_GV10X;
    pDTLCI->ibCertDigest                                         = OEM_TEE_LPROV_DEVICECERT_CERTDIGEST_OFFSET_SL3000_GV10X;
    pDTLCI->ibSignData                                           = OEM_TEE_LPROV_DEVICECERT_SIGNDATA_START_OFFSET_SL3000_GV10X;
    pDTLCI->cbSignData                                           = OEM_TEE_LPROV_DEVICECERT_SIGNDATA_LENGTH_SL3000_GV10X;
    pDTLCI->ibSignature                                          = OEM_TEE_LPROV_DEVICECERT_SIGNATURE_OFFSET_SL3000_GV10X;
    pDTLCI->rgibPubKeys[ DRM_TEE_LPROV_PUBKEY_SIGN_IDX ]         = OEM_TEE_LPROV_DEVICECERT_PUBKEY_SIGN_OFFSET_SL3000_GV10X;
    pDTLCI->rgibPubKeys[ DRM_TEE_LPROV_PUBKEY_ENCRYPT_IDX ]      = OEM_TEE_LPROV_DEVICECERT_PUBKEY_ENCRYPT_OFFSET_SL3000_GV10X;
#ifndef PR_V0404
    pDTLCI->rgibPubKeys[ DRM_TEE_LPROV_PUBKEY_PRND_ENCRYPT_IDX ] = OEM_TEE_LPROV_DEVICECERT_PUBKEY_PRND_ENCRYPT_OFFSET_SL3000_GV10X;
#endif
}

/*!
 * @brief  Ensure PR is not running with revoked ACR, scrubber, UDE, gsplite ucodes
 *
 * @return FLCN_OK if there is no revoked ucode
 *         FLCN_ERR_HS_PR_MPK_DEC_NEEDS_NEWER_ACR_UDE_SCRUBBER if there is any revoked ucode
 *         Or bubble up the error code returned from the callee
 */
FLCN_STATUS
prCheckDependentBinMilwersion_GV10X(void)
{
    FLCN_STATUS  status;
    LwU32        lwrrAcrVer      = 0;
    LwU32        lwrrUdeVer      = 0;
    LwU32        lwrrScrubberVer = 0;
    LwU32        lwrrGspliteVer  = 0;
    LwU32        minAcrVer       = 0xFFFFFFFF;
    LwU32        minUdeVer       = 0xFFFFFFFF;
    LwU32        minScrubberVer  = 0xFFFFFFFF;
    LwU32        minGspliteVer   = 0xFFFFFFFF;
    LwU32        tmp;

    // Read the current versions of ACR, UDE, scrubber and gsplite
    // XXX: Need to figure out a macro to collapse the following 3 lines into one
    status = BAR0_REG_RD32_HS_ERRCHK(LW_PGC6_BSI_SELWRE_SCRATCH_14, &tmp);
    if (status != FLCN_OK)
    {
        goto ErrExit;
    }
    lwrrAcrVer = DRF_VAL(_PGC6, _BSI_VPR_SELWRE_SCRATCH_14, _ACR_BINARY_VERSION, tmp);

    status = BAR0_REG_RD32_HS_ERRCHK(LW_PGC6_BSI_SELWRE_SCRATCH_15, &tmp);
    if (status != FLCN_OK)
    {
        goto ErrExit;
    }
    lwrrUdeVer      = DRF_VAL(_PGC6, _BSI_VPR_SELWRE_SCRATCH_15, _VBIOS_UDE_VERSION, tmp);
    lwrrScrubberVer = DRF_VAL(_PGC6, _BSI_VPR_SELWRE_SCRATCH_15, _SCRUBBER_BINARY_VERSION, tmp);

    status = BAR0_REG_RD32_HS_ERRCHK(LW_PGC6_AON_SELWRE_SCRATCH_GROUP_04_00, &tmp);
    if (status != FLCN_OK)
    {
        goto ErrExit;
    }
    lwrrGspliteVer = DRF_VAL(_PGC6, _AON_SELWRE_SCRATCH_GROUP_04_00, _GSP_UCODE_VERSION, tmp);

    status = BAR0_REG_RD32_HS_ERRCHK(LW_PMC_BOOT_42, &tmp);
    if (status != FLCN_OK)
    {
        goto ErrExit;
    }

    //
    // Compute the min required versions for ACR, UDE, scrubber and GSPLite
    //
    // The numbers have been obtained as follows
    // ACR: Adding one to the values found at https://rmopengrok.lwpu.com/source/xref/r384_00/uproc/acr/src/acr/volta/acrucgv100.c#acrGetUcodeBuildVersion_GV100 as of 11/13/2017
    // UDE: Looking up fuse.lwpu.com and picking the least value across all shipping SKUs for each chip. even if SW version was bumped after this, unless we can force update vbios in the field, we are stuck to lowest shipping value
    // Scrubber: Adding one to the values found at https://rmopengrok.lwpu.com/source/xref/r384_00/uproc/selwrescrub/config/selwrescrub-profiles.mk#SELWRESCRUB_HS_UCODE_VERSION as of 11/13/2017
    // GSPLite: Adding one to the values found at https://rmopengrok.lwpu.com/source/xref/r384_00/uproc/disp/dpu/inc/gsp_hs.h#_dpuGetFuseVersionSW_GV10X as of 11/13/2017
    //
    tmp = DRF_VAL(_PMC, _BOOT_42, _CHIP_ID, tmp);
    if (tmp == LW_PMC_BOOT_42_CHIP_ID_GV100)
    {
        minAcrVer      = 6;
        minUdeVer      = 7;
        minScrubberVer = 2;
        minGspliteVer  = 3;
    }

    // Ensure that ACR, UDE, scrubber and gsplite meet the min version criteria
    if ((lwrrAcrVer < minAcrVer) ||
        (lwrrUdeVer < minUdeVer) ||
        (lwrrScrubberVer < minScrubberVer) ||
        (lwrrGspliteVer < minGspliteVer))
    {
        status = FLCN_ERR_HS_PR_MPK_DEC_NEEDS_NEWER_ACR_UDE_SCRUBBER;
        goto ErrExit;
    }

ErrExit:
    return status;
}

