status_t run_rsa_sign_tests(crypto_context_t *crypto_ctx, engine_id_t engine_id)
{
	status_t ret = NO_ERROR;

#if HAVE_RSA_PKCS_SIGN
#if RSA_MIN_KEYSIZE_BITS <= 512U
	// 512 bit RSA sign/verify test (PKA0 && PKA1)

#if HAVE_SHA1
	TEST_ERRCHK(TEST_rsa512_sign, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA1);	// SE0 RSA0
#endif
#if HAVE_SHA224
	TEST_ERRCHK(TEST_rsa512_sign, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA224);
#endif

	TEST_ERRCHK(TEST_rsa512_sign, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA256);
	// SHA-384 and SHA-512 digests will not fit in RSA_512 signature
	TEST_EXPECT_RET(TEST_rsa512_sign, ERR_BAD_LEN, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA384);
	TEST_EXPECT_RET(TEST_rsa512_sign, ERR_BAD_LEN, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA512);
#endif /* RSA_MIN_KEYSIZE_BITS <= 512U */

	// 2048 bit RSA sign/verify test
	//
#if HAVE_SHA1
	TEST_ERRCHK(TEST_rsa2048_sign, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA1);
#endif
#if HAVE_SHA224
	TEST_ERRCHK(TEST_rsa2048_sign, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA224);
#endif
	TEST_ERRCHK(TEST_rsa2048_sign, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA256);
	TEST_ERRCHK(TEST_rsa2048_sign, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA384);
	TEST_ERRCHK(TEST_rsa2048_sign, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA512);

	// 4096 bit RSA sign/verify test (auto-select PKA1 even when PKA0 suggested)
	//
#if HAVE_SHA1
	TEST_ERRCHK(TEST_rsa4096_sign, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA1);
#endif
#if HAVE_SHA224
	TEST_ERRCHK(TEST_rsa4096_sign, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA224);
#endif
	TEST_ERRCHK(TEST_rsa4096_sign, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA256);
	TEST_ERRCHK(TEST_rsa4096_sign, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA384);
	TEST_ERRCHK(TEST_rsa4096_sign, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA512);

	// 3072 bit RSA sign/verify using keyslots (PKA1 only, autoselected if PKA0 suggested)
#if HAVE_SHA1
	TEST_ERRCHK(TEST_rsa3072_sign_keyslot, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA1);
#endif
#if HAVE_SHA224
	TEST_ERRCHK(TEST_rsa3072_sign_keyslot, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA224);
#endif
	TEST_ERRCHK(TEST_rsa3072_sign_keyslot, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA256);
	TEST_ERRCHK(TEST_rsa3072_sign_keyslot, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA384);
	TEST_ERRCHK(TEST_rsa3072_sign_keyslot, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA512);

	// 2048 bit RSA sign/verify with either PKA1 or SE PKA0
	TEST_ERRCHK(TEST_rsa2048_sign_keyslot, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA384);
#endif /* HAVE_RSA_PKCS_SIGN */

#if HAVE_RSA_PKCS_VERIFY
	TEST_ERRCHK(TEST_rsa4096_verify_sha512_pkcs1_v1_5, engine_id, TE_ALG_RSASSA_PKCS1_V1_5_SHA512);
#endif

#if HAVE_RSA_PSS_SIGN
#if RSA_MIN_KEYSIZE_BITS <= 512U
	// 512 bit RSA sign/verify test (PKA0 && PKA1)
	//
#if HAVE_SHA1
	TEST_ERRCHK(TEST_rsa512_sign, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA1);	// SE0 RSA0
#endif
#if HAVE_SHA224
	TEST_ERRCHK(TEST_rsa512_sign, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA224);
#endif

	// SHA-256 PSS encoding does not fit to 512 bit RSA signature when SALT LENGTH equals DIGEST LENGTH
	// TODO: Support smaller salt lengths (e.g. zero) in RSA context init
	//
	TEST_EXPECT_RET(TEST_rsa512_sign, ERR_BAD_LEN, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA256);

	// SHA-384 and SHA-512 digests will not fit in RSA_512 signature
	TEST_EXPECT_RET(TEST_rsa512_sign, ERR_BAD_LEN, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA384);
	TEST_EXPECT_RET(TEST_rsa512_sign, ERR_BAD_LEN, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA512);
#endif /* RSA_MIN_KEYSIZE_BITS <= 512U */

	// 2048 bit RSA sign/verify test (PKA0 && PKA1)
	//
#if HAVE_SHA1
	TEST_ERRCHK(TEST_rsa2048_sign, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA1);
#endif
#if HAVE_SHA224
	TEST_ERRCHK(TEST_rsa2048_sign, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA224);
#endif
	TEST_ERRCHK(TEST_rsa2048_sign, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA256);
	TEST_ERRCHK(TEST_rsa2048_sign, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA384);
	TEST_ERRCHK(TEST_rsa2048_sign, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA512);

	// 4096 bit RSA sign/verify test (PKA1 only, autoselected if PKA0 suggested)
	//
#if HAVE_SHA1
	TEST_ERRCHK(TEST_rsa4096_sign, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA1);
#endif
#if HAVE_SHA224
	TEST_ERRCHK(TEST_rsa4096_sign, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA224);
#endif
	TEST_ERRCHK(TEST_rsa4096_sign, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA256);
	TEST_ERRCHK(TEST_rsa4096_sign, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA384);
	TEST_ERRCHK(TEST_rsa4096_sign, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA512);

	// 3072 bit RSA sign/verify using keyslots (PKA1 only, autoselected if PKA0 suggested)
#if HAVE_SHA1
	TEST_ERRCHK(TEST_rsa3072_sign_keyslot, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA1);
#endif
#if HAVE_SHA224
	TEST_ERRCHK(TEST_rsa3072_sign_keyslot, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA224);
#endif
	TEST_ERRCHK(TEST_rsa3072_sign_keyslot, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA256);
	TEST_ERRCHK(TEST_rsa3072_sign_keyslot, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA384);
	TEST_ERRCHK(TEST_rsa3072_sign_keyslot, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA512);

	// 2048 bit RSA sign/verify with either PKA1 or SE PKA0
	TEST_ERRCHK(TEST_rsa2048_sign_keyslot, engine_id, TE_ALG_RSASSA_PKCS1_PSS_MGF1_SHA256);
#endif /* HAVE_RSA_PSS_SIGN */

	if (CFALSE) {
	fail:
		LOG_ERROR("[ ***ERROR: SE RSA SIGN TEST CASE FAILED! (err 0x%x) ]\n",ret);
		// On failure must return error code
		if (NO_ERROR == ret) {
			ret = SE_ERROR(ERR_GENERIC);
		}
	}
	return ret;
}

