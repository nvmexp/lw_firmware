static status_t CCM_test_cipher(crypto_context_t *c, uint32_t count,
				const char *vector_name, engine_id_t engine_id,
				te_crypto_alg_mode_t mode,
				uint32_t keybytes, const uint8_t *key,
				const uint8_t *input, uint32_t input_len,
				const uint8_t *output, uint32_t output_len,
				const uint8_t *nonce, uint32_t nonce_len,
				const uint8_t *aad, uint32_t aad_len,
				uint32_t tag_len)
{
	status_t ret = NO_ERROR;
	te_crypto_args_t arg = { .ca_handle = 0U, };
	static uint8_t result[512U];	// XXXX AES-CCM implementation has a size limit (FIXME)
	const char *mode_str = "(ENCRYPT)";

	static te_args_key_data_t akey = {
		.k_key_type = KEY_TYPE_AES,
		.k_flags = KEY_FLAG_PLAIN,
		.k_keyslot = AES_KEYSLOT,
	};

	if (NULL == vector_name) {
		vector_name = "";
	}

	if (TE_ALG_MODE_DECRYPT == mode) {
		mode_str = "(DECRYPT)";
	}

	(void)mode_str;

	LOG_ALWAYS("\n[ AES-CCM vector test: %s %s %u START ]\n", vector_name, mode_str, count);

	// input and aad may be NULL
	//
	if ((NULL == c) || (NULL == output) || (NULL == key)) {
		CCC_ERROR_WITH_ECODE(ERR_ILWALID_ARGS);
	}

	ret = crypto_kernel_context_setup(c, 0x42, mode, TE_ALG_AES_CCM);
	CCC_ERROR_CHECK(ret, LTRACEF("context setup failed\n"));

	se_util_mem_set(result, 0, sizeof_u32(result));

	if (0U == input_len) {
		input = NULL;
	}

	if (0U == aad_len) {
		aad = NULL;
	}

	arg.ca_algo     = TE_ALG_AES_CCM;
	arg.ca_alg_mode = mode;
	arg.ca_opcode   = TE_OP_COMBINED_OPERATION;

	arg.ca_init.engine_hint = engine_id;

	/* Set AES-CCM algorithm init values
	 */
	se_util_mem_move(arg.ca_init.aes.ccm_mode.nonce, nonce, nonce_len);
	arg.ca_init.aes.ccm_mode.nonce_len = nonce_len;

	arg.ca_init.aes.ccm_mode.aad = aad;
	arg.ca_init.aes.ccm_mode.aad_len = aad_len;

	arg.ca_init.aes.ccm_mode.tag_len = tag_len;

	akey.k_byte_size = keybytes;
	se_util_mem_move(akey.k_aes_key, key, akey.k_byte_size);

	arg.ca_set_key.kdata = &akey;

	arg.ca_data.src_size = input_len;
	arg.ca_data.src = input;
	arg.ca_data.dst_size = sizeof_u32(result);
	arg.ca_data.dst = result;

	ret = crypto_handle_operation(c, &arg);
	CCC_ERROR_CHECK(ret,
			LOG_ERROR("%s: CRYPTO(%s): syscall (0x%x); ALGO: AES-CCM, key: %u, MODE: %u\n",
				  vector_name, __func__, ret, (keybytes * 8U), mode));

	TRAP_ASSERT(arg.ca_data.dst_size == output_len);

	if (0U != memory_compare(result, output, output_len)) {
		LOG_ERROR("%s: CRYPTO(%s): result compare failed; ALGO: AES-CCM, key: %u, MODE: %u\n",
			  vector_name, __func__,  (keybytes * 8U), mode);
		LOG_HEXBUF(vector_name, result, sizeof_u32(result));
		LOG_HEXBUF("callwlated (wrong)", output, arg.ca_data.dst_size);

		CCC_ERROR_WITH_ECODE(ERR_BAD_STATE);
	}

	LOG_ALWAYS("[ AES-CCM vector test: %s %s %u PASSED ]\n", vector_name, mode_str, count);

fail:
	if (NO_ERROR != ret) {
		LOG_ERROR("[ AES-CCM vector test: %s %s %u FAILED: 0x%x]\n",
			  vector_name, mode_str, count, ret);
	}

	if (NULL != c) {
		crypto_kernel_context_reset(c);
	}

	return ret;
}

static status_t CCM_test_cipher_with_keyslot(crypto_context_t *c, uint32_t count,
					     const char *vector_name, engine_id_t engine_id,
					     te_crypto_alg_mode_t mode,
					     uint32_t keybytes, const uint8_t *key,
					     const uint8_t *input, uint32_t input_len,
					     const uint8_t *output, uint32_t output_len,
					     const uint8_t *nonce, uint32_t nonce_len,
					     const uint8_t *aad, uint32_t aad_len,
					     uint32_t tag_len,
					     bool set_key)
{
	status_t ret = NO_ERROR;
	te_crypto_args_t arg = { .ca_handle = 0U, };
	static uint8_t result[512U];	// XXXX AES-CCM implementation has a size limit (FIXME)
	const char *mode_str = "(ENCRYPT)";
	const char *ks_mode = "SET";

	static te_args_key_data_t akey = {
		.k_key_type = KEY_TYPE_AES,
		.k_flags = KEY_FLAG_USE_KEYSLOT_KEY | KEY_FLAG_LEAVE_KEY_IN_KEYSLOT,
		.k_keyslot = AES_KEYSLOT,
	};

	(void)ks_mode;

	if (NULL == vector_name) {
		vector_name = "";
	}

	if (TE_ALG_MODE_DECRYPT == mode) {
		mode_str = "(DECRYPT)";
	}

	(void)mode_str;

	if (BOOL_IS_TRUE(set_key)) {
		ks_mode = "USE";
	}

	LOG_ALWAYS("\n[ AES-CCM+%s keyslot vector test: %s %s %u START ]\n",
		   ks_mode, vector_name, mode_str, count);

	// input and aad may be NULL
	//
	if ((NULL == c) || (NULL == output) || (NULL == key)) {
		CCC_ERROR_WITH_ECODE(ERR_ILWALID_ARGS);
	}

	ret = crypto_kernel_context_setup(c, 0x42, mode, TE_ALG_AES_CCM);
	CCC_ERROR_CHECK(ret, LTRACEF("context setup failed\n"));

	se_util_mem_set(result, 0, sizeof_u32(result));

	if (0U == input_len) {
		input = NULL;
	}

	if (0U == aad_len) {
		aad = NULL;
	}

	if (BOOL_IS_TRUE(set_key)) {
		LTRACEF("AES-CCM setting key to keyslot %u\n", AES_KEYSLOT);
		ret = se_set_device_aes_keyslot(SE_CDEV_SE0, AES_KEYSLOT,
						key, (keybytes * 8U));
		CCC_ERROR_CHECK(ret, LTRACEF("key setup failed\n"));
	}

	arg.ca_algo     = TE_ALG_AES_CCM;
	arg.ca_alg_mode = mode;
	arg.ca_opcode   = TE_OP_COMBINED_OPERATION;

	arg.ca_init.engine_hint = engine_id;

	/* Set AES-CCM algorithm init values
	 */
	se_util_mem_move(arg.ca_init.aes.ccm_mode.nonce, nonce, nonce_len);
	arg.ca_init.aes.ccm_mode.nonce_len = nonce_len;

	arg.ca_init.aes.ccm_mode.aad = aad;
	arg.ca_init.aes.ccm_mode.aad_len = aad_len;

	arg.ca_init.aes.ccm_mode.tag_len = tag_len;

	akey.k_byte_size = keybytes;

	arg.ca_set_key.kdata = &akey;

	arg.ca_data.src_size = input_len;
	arg.ca_data.src = input;
	arg.ca_data.dst_size = sizeof_u32(result);
	arg.ca_data.dst = result;

	ret = crypto_handle_operation(c, &arg);
	CCC_ERROR_CHECK(ret,
			LOG_ERROR("%s: CRYPTO(%s): syscall (0x%x); ALGO: AES-CCM, key: %u, MODE: %u\n",
				  vector_name, __func__, ret, (keybytes * 8U), mode));

	TRAP_ASSERT(arg.ca_data.dst_size == output_len);

	if (0U != memory_compare(result, output, output_len)) {
		LOG_ERROR("%s: CRYPTO(%s): result compare failed; ALGO: AES-CCM, key: %u, MODE: %u\n",
			  vector_name, __func__,  (keybytes * 8U), mode);
		LOG_HEXBUF(vector_name, result, sizeof_u32(result));
		LOG_HEXBUF("callwlated (wrong)", output, arg.ca_data.dst_size);

		CCC_ERROR_WITH_ECODE(ERR_BAD_STATE);
	}

	LOG_ALWAYS("[ AES-CCM+%s keyslot vector test: %s %s %u PASSED ]\n",
		   ks_mode, vector_name, mode_str, count);

fail:
	if (NO_ERROR != ret) {
		LOG_ERROR("[ AES-CCM+%s keyslot vector test: %s %s %u FAILED: 0x%x]\n",
			  ks_mode, vector_name, mode_str, count, ret);
	}

	if (NULL != c) {
		crypto_kernel_context_reset(c);
	}

	return ret;
}

/* Run one AES-CCM test */
#define CCM_TEST_DYNKEY(nname, cmode, input, output, nonce, aad, tag_len)	\
	do { ret = CCM_test_cipher(crypto_ctx, COUNT, nname, engine_id,	\
				   TE_ALG_MODE_##cmode, sizeof_u32(Key), Key, \
				   input, sizeof_u32(input),		\
				   output, sizeof_u32(output),		\
				   nonce, sizeof_u32(nonce),		\
				   aad, (NULL == aad ? 0 : sizeof_u32(aad)), \
				   tag_len);				\
		CCC_ERROR_CHECK(ret,					\
				LOG_ERROR("[ AES-CCM vector test %s (line %u) FAILED (0x%x)]\n", \
					  (nname?nname:""), __LINE__, ret)); \
	} while(false)

#define CCM_TEST_KEYSLOT(nname, cmode, input, output, nonce, aad, tag_len)	\
	do { ret = CCM_test_cipher_with_keyslot(crypto_ctx, COUNT, nname, engine_id,	\
				   TE_ALG_MODE_##cmode, sizeof_u32(Key), Key, \
				   input, sizeof_u32(input),		\
				   output, sizeof_u32(output),		\
				   nonce, sizeof_u32(nonce),		\
				   aad, (NULL == aad ? 0 : sizeof_u32(aad)), \
				   tag_len,				\
				   true);				\
		CCC_ERROR_CHECK(ret,					\
				LOG_ERROR("[ AES-CCM set key vector test %s (line %u) FAILED (0x%x)]\n", \
					  (nname?nname:""), __LINE__, ret)); \
	} while(false)

#define CCM_TEST_OLD_KEYSLOT(nname, cmode, input, output, nonce, aad, tag_len)	\
	do { ret = CCM_test_cipher_with_keyslot(crypto_ctx, COUNT, nname, engine_id,	\
				   TE_ALG_MODE_##cmode, sizeof_u32(Key), Key, \
				   input, sizeof_u32(input),		\
				   output, sizeof_u32(output),		\
				   nonce, sizeof_u32(nonce),		\
				   aad, (NULL == aad ? 0 : sizeof_u32(aad)), \
				   tag_len,				\
				   false);				\
		CCC_ERROR_CHECK(ret,					\
				LOG_ERROR("[ AES-CCM old key vector test %s (line %u) FAILED (0x%x)]\n", \
					  (nname?nname:""), __LINE__, ret)); \
	} while(false)

/* Test cipher/decipher with dynamic key and with key preset to keyslot
 */
#define CCM_TEST(nname, cmode, input, output, nonce, aad, tag_len)	\
	CCM_TEST_DYNKEY(nname, cmode, input, output, nonce, aad, tag_len); \
	CCM_TEST_KEYSLOT(nname, cmode, input, output, nonce, aad, tag_len); \
	CCM_TEST_OLD_KEYSLOT(nname, cmode, input, output, nonce, aad, tag_len);

